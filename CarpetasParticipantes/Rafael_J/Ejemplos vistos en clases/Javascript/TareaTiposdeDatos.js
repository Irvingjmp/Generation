/* TIPOS DE DATOS */

/*Un tipo de dato es la forma en la que vamos a pasar información del mundo real a nuesto codigo

existen 9 tipos de datos en javascript de los cuales 6 son considerados como "primitivos" porque son tipos de valor simples, indivisibles, y no están basados en otros objetos o estructuras de datos. 
Los valores primitivos son inmutables, es decir, no pueden ser modificados una vez definidos. Cada vez que se realiza una operación sobre ellos, se genera un nuevo valor en lugar de modificar el valor existente.

9 tipos de datos:

    Primitivos:

1- string: este es para representar cadenas de texto.
2- number: para valores numericos, sin importar si estos son decimales.
3- bigint: designado para valores numericos enteros grandes.
4- boolean: para valores de true o false
5- symbol: para crear identificadores unicos que nos ayuden a no tener conflictos en las claves.
6- undefined: indica una variable declarada pero no asignada

    No primitivos:

7- null: representa la ausencia intencionada de cualquier valor. Es un tipo especial que significa "vacío" o "sin valor". 
   Suele usarse para indicar que una variable debería tener un valor, pero actualmente no lo tiene.
8- object: estructura de datos compleja que permite almacenar colecciones de datos y entidades más complejas. 
   Un objeto se define mediante pares de clave-valor, donde las claves son los nombres de las propiedades, y los valores pueden ser cualquier tipo de dato, incluidos otros objetos y funciones
9- function: es un tipo de objeto pero es invocable o ejecutable en codigo.

NaN: significa "Not-a-Number" y es un valor especial que representa un resultado indefinido o erroneo de una operacion aritmetica.

*/